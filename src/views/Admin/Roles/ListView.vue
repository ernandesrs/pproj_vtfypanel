<template>
	<loading-elem v-if="loadingContent"></loading-elem>

	<template v-else>
		<actions-bar bar-title="Funções" :action-button-create="{
			show: true,
			disabled: !computed_userStore.permission('role').canCreate(),
			text: 'Nova função',
			icon: 'mdi-shield-plus-outline',
			to: { name: 'admin.roles.create' },
		}"></actions-bar>

		<div>
			<p>Pode listar usuários: {{ computed_userStore.permissions('role').canViewAny() }}</p>
			<p>Pode ver usuário: {{ computed_userStore.permissions('role').canView() }}</p>
			<p>Pode criar usuário: {{ computed_userStore.permissions('role').canCreate() }}</p>
			<p>Pode atualizar usuário: {{ computed_userStore.permissions('role').canUpdate() }}</p>
			<p>Pode excluir usuário: {{ computed_userStore.permissions('role').canDelete() }}</p>
			<p>Pode excluir forçado usuário: {{ computed_userStore.permissions('role').canForceDelete() }}</p>
			<p>Pode recuperar usuário: {{ computed_userStore.permissions('role').canRecovery() }}</p>
		</div>

		<list-group-elem @changePage="method_changePage" resource="role" :items="roles.list" :pages="roles.pages"
			v-slot="{ item }" :action-edit="method_edit" :action-delete="method_deleteConfirmed"
			action-delete-dialog-title="Excluir esta função?"
			action-delete-dialog-text="A exclusão desta função será definitiva e não poderá ser desfeita."
			:action-filter="method_filter">

			<v-row>
				<v-col cols="12" sm="8" md="4" lg="3">
					<p class="text-subtitle-2 font-weight-light">Nome da função</p>
					<p class="font-weight-medium">{{ item.display_name }}</p>
					<p></p>
				</v-col>
				<v-col cols="12" sm="4" md="3">
					<p class="text-subtitle-2 font-weight-light">Nome único</p>
					<p class="font-weight-medium">{{ item.name }}</p>
					<p></p>
				</v-col>
			</v-row>

		</list-group-elem>
	</template>
</template>

<script>

import { useAppStore } from '@/store/app';
import { useUserStore } from '@/store/user';
import axios from '@/plugins/axios.js';
import alert from '@/services/alert.js';
import LoadingElem from '@/components/LoadingElem.vue';
import ActionsBar from '@/layouts/default/ActionsBar.vue';
import ListGroupElem from '@/components/ListGroupElem.vue';
import { useUserStore } from '@/store/user';

export default {
	components: { LoadingElem, ActionsBar, ListGroupElem },
	data() {
		return {
			loadingContent: true,
			roles: {
				limit: 10,
				list: [],
				pages: []
			}
		};
	},
	created() {
		this.method_main();
	},
	methods: {
		method_main() {
			this.computed_appStore.updateBreadcrumbs([
				{
					text: 'Funções',
					to: { name: 'admin.roles' },
					disabled: true
				}
			]);

			this.method_getRoles(1);
		},
		method_changePage(page) {
			return this.method_getRoles(page.page);
		},
		method_getRoles(page, search = null) {
			let action = '/admin/roles?page=' + page + '&limit=' + this.roles.limit + (search ? '&search=' + search : '');

			return axios.req({
				action: action,
				method: 'get',
				success: (resp) => {
					this.roles.list = resp.data.roles.list;
					this.roles.pages = resp.data.roles.meta.links;
				},
				finally: () => {
					this.loadingContent = false;
				}
			});
		},
		method_edit(event) {
			let id = event.target.getAttribute('data-identificator');
			this.$router.push({ name: 'admin.roles.edit', params: { role_id: id } });
		},
		method_deleteConfirmed(event) {
			let id = event.target.getAttribute('data-identificator');

			return axios.req({
				action: '/admin/roles/' + id,
				method: 'delete',
				success: () => {
					alert.add('Função excluída com sucesso!',
						'warning',
						'Excluída!',
						null,
						false
					);

					this.roles.list.splice(this.roles.list.findIndex((role) => role.id == id), 1);
				}
			});
		},
		method_filter(event) {
			return this.method_getRoles(1, event.search);
		}
	},
	computed: {
		computed_appStore() {
			return useAppStore();
		},
		computed_userStore() {
			return useUserStore();
		}
	}
}

</script>